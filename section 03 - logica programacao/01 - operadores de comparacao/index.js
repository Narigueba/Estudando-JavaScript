/*
    Operadores: 
    > maior que
    >= maior ou igual a
    < menor que
    <= menor ou igual a
    == igualdade (valor)
    === igualdade restrita (valor e tipo)
    != diferente (valor)
    !== diferente restrito (valor e tipo)
*/

console.log(10 > 5); // true
console.log(10 >= 10); // true
console.log(10 < 10); // false
console.log(10 <= 10); // true
console.log(10 == '10'); // true
console.log(10 === '10'); // false
console.log(10 != '10'); // false
console.log(10 !== '10'); // true